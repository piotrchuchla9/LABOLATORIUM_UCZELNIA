package LAB10.Adres;

public class Adres {

    String ulica;
    String numer_budynku;
    String numer_mieszkania;
    String kod;
    String miejscowosc;

    public Adres(String ulica, String numer_budynku, String numer_mieszkania, String kod, String miejscowosc){
        this.ulica = ulica;
        this.numer_budynku = numer_budynku;
        this.numer_mieszkania = numer_mieszkania;
        this.kod = kod;
        this.miejscowosc = miejscowosc;
    }
}