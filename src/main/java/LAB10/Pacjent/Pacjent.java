package LAB10.Pacjent;

public class Pacjent {
    String imie;
    String nazwisko;
    int dzien;
    int miesiac;
    int rok;

    public Pacjent(String imie, String nazwisko, int dzien, int miesiac, int rok){
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.dzien = dzien;
        this.miesiac = miesiac;
        this.rok = rok;
    }
}