package LAB7.Firma;

public class Firma {

    String nazwaFirmy;
    String adresFirmy;
    String właścicielFirmy;

    public String getNazwaFirmy() {
        return this.nazwaFirmy;
    }

    public void setNazwaFirmy(String nazwaFirmy) {
        this.nazwaFirmy = nazwaFirmy;
    }

    public String getAdresFirmy() {
        return this.adresFirmy;
    }

    public void setAdresFirmy(String adresFirmy) {
        this.adresFirmy = adresFirmy;
    }

    public String getWłaścicielFirmy() {
        return this.właścicielFirmy;
    }

    public void setWłaścicielFirmy(String właścicielFirmy) {
        this.właścicielFirmy = właścicielFirmy;
    }

}
