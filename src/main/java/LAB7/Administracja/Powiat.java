package LAB7.Administracja;

public class Powiat {

    String nazwaPowiatu;
    int liczebnośćPowiatu;

    public String getNazwaPowiatu() {
        return nazwaPowiatu;
    }

    public void setNazwaPowiatu(String nazwaPowiatu) {
        this.nazwaPowiatu = nazwaPowiatu;
    }

    public int getLiczebnośćPowiatu() {
        return liczebnośćPowiatu;
    }

    public void setLiczebnośćPowiatu(int liczebnośćPowiatu) {
        this.liczebnośćPowiatu = liczebnośćPowiatu;
    }

}
